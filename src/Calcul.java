

public class Calcul {

    public static void main(String[] args) {
        int longeur = 250;
        int largeur = 140;


        int Surface = longeur * largeur;
        int Perimetre = 2 * (longeur + largeur);

        System.out.printf("longeur : %d m \nlargeur : %d m \n" +
                "Surface : %d m2 \nPerimetre : %d m", longeur
                , largeur, Surface, Perimetre);

    }





}
